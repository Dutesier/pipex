#include "../includes/ft_pipex.h"

int ft_strcmp(char *str1, char *str2, int cmp)
{
	int i;

	i = 0;
	while(i < cmp)
	{
		if (str1[i] != str2[i])
			return (0);
		i++;
	}
	return (1);
}
